def main():
    # Loop to count from 50 to 1
    for i in range(50, 0, -1):  # Reverse countdown from 10 to 1
        print(i, end=" ")  # Print in the same line

    # After the countdown ends, print 'Liftoff!'
    print("Liftoff!")

# Call the main function
if __name__ == '__main__':
    main()
